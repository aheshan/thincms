/**
 * double given integer number
 * @param integer $number
 * @return number
 */
function double_number($number){
	return $number * 2;
}

/**
 * Sum given array
 * 
 * @param unknown $array_data
 * @return number
 */
function sum_array($array_data){
	return array_sum($array_data);
}
